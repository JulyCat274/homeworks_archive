example = "Монитор"
# вывести первый символ строки
print(example[0])

# вывести последний символ строки, используя отрицательный индекс
print(example[-1])

# вывести вторую половину строки (итор)
print(example[3:])

# вывести слово наоборот
print(example[::-1])

# вывести каждый второй символ строки (оио)
print(example[1::2])